int	ft_count_words(const char *s, char c)
{
	int cunt;

	cunt = 0;
	while (*s)
	{
		while (*s == c && *s)
			s++;
		if (*s)
			cunt++;
		while (*s != c && *s)
			s++;
	}
	return (cunt);
}
