#include <string.h>

size_t	ft_size_digit(long n)
{
	size_t i;

	i = 0;
	if (n < 0)
	{
		i = 1;
		n = -n;
	}
	while (n != 0)
	{
		n /= 10;
		i++;
	}
	return (i++);
}
